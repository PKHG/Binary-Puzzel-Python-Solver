def convertToLStr(oldpuzzel):
    """
    The Z3 solver needs a long string as input"
    thus concatenate the rows:
    """
    if not type(oldpuzzel) == type([]):
        return ('not a list: ' , oldpuzzel)
    else:
        tmp = ""
        for i in range(len(oldpuzzel)):
            tmp += oldpuzzel[i]
        return tmp
    
